const metrics = (
  strength: number,
  bitterness: number,
  acidity: number,
  sweetness: number,
  body: number,
): Record<CoffeeMetricId, number> => ({
  strength,
  bitterness,
  acidity,
  sweetness,
  body,
});

const layer = (
  id: keyof typeof layerPalette,
  label: string,
  amountPercent: number,
): CoffeeLayer => ({
  id,
  label,
  amountPercent,
  color: layerPalette[id],
});

